import sys
import os
from pathlib import Path


project_root = Path(__file__).parent.parent
sys.path.append(str(project_root))

from docx import Document
from docx.shared import Pt

def generate_anxiety_document():
    """Генерация документа про тревожность"""
    doc = Document()
    
    
    style = doc.styles['Normal']
    font = style.font
    font.name = 'Times New Roman'
    font.size = Pt(14)
    
    
    title = doc.add_heading('Тревожность: Причины, Симптомы и Методы Преодоления', 0)
    
    
    doc.add_heading('Введение', level=1)
    doc.add_paragraph(
        'Тревожность - это естественная эмоциональная реакция на стрессовые ситуации, '
        'которая характеризуется чувством беспокойства, напряжения и ожидания негативных событий. '
        'В умеренных количествах тревожность выполняет защитную функцию, помогая нам '
        'подготовиться к потенциальным угрозам. Однако когда тревога становится чрезмерной '
        'или хронической, она может серьезно нарушить качество жизни.'
    )
    
    
    doc.add_heading('Причины Тревожности', level=1)
    doc.add_paragraph(
        'Тревожность может возникать по различным причинам, которые часто сочетаются между собой:'
    )
    
    causes = [
        'Генетическая предрасположенность и семейная история тревожных расстройств',
        'Химический дисбаланс нейромедиаторов в головном мозге',
        'Травматические события и стрессовые жизненные ситуации',
        'Особенности личности и темперамента',
        'Хронические заболевания и физические недуги',
        'Злоупотребление психоактивными веществами'
    ]
    
    for cause in causes:
        doc.add_paragraph(cause, style='List Bullet')
    
    
    doc.add_heading('Симптомы Тревожности', level=1)
    doc.add_paragraph('Тревожность проявляется через различные физические и психологические симптомы:')
    
    symptoms = [
        'Учащенное сердцебиение и пульс',
        'Повышенное потоотделение и дрожь в теле',
        'Затрудненное дыхание или ощущение удушья',
        'Мышечное напряжение и боли',
        'Бессонница и нарушения сна',
        'Трудности с концентрацией внимания',
        'Постоянное беспокойство и предчувствие беды',
        'Избегание ситуаций, вызывающих тревогу'
    ]
    
    for symptom in symptoms:
        doc.add_paragraph(symptom, style='List Bullet')
    
    
    doc.add_heading('Методы Преодоления Тревожности', level=1)
    doc.add_paragraph(
        'Существует множество эффективных стратегий для управления тревожностью:'
    )
    
    methods = [
        'Когнитивно-поведенческая терапия (КПТ)',
        'Техники релаксации и медитации',
        'Регулярная физическая активность',
        'Сбалансированное питание и здоровый сон',
        'Ограничение потребления кофеина и алкоголя',
        'Практика осознанности (mindfulness)',
        'Ведение дневника тревожности',
        'Обращение за профессиональной помощью'
    ]
    
    for method in methods:
        doc.add_paragraph(method, style='List Bullet')
    
    
    doc.add_heading('Заключение', level=1)
    doc.add_paragraph(
        'Тревожность - это распространенное состояние, с которым сталкиваются многие люди. '
        'Важно помнить, что обращение за помощью является признаком силы, а не слабости. '
        'Современные методы терапии и самопомощи позволяют эффективно управлять тревожностью '
        'и вернуться к полноценной жизни. Если вы испытываете симптомы тревожности, '
        'не стесняйтесь обратиться к специалисту для получения квалифицированной помощи.'
    )
    
    documents_dir = project_root / 'documents'
    documents_dir.mkdir(exist_ok=True)
    
    document_path = documents_dir / 'тревожность.docx'
    doc.save(document_path)
    print(f" Документ создан: {document_path}")

if __name__ == "__main__":
    generate_anxiety_document()